class ShoppingCart {
    constructor() {
        this.items = [];
        this.loadFromStorage();
    }
    
    addItem(product) {
        const existingIndex = this.items.findIndex(item => item.code === product.code);
        if (existingIndex !== -1) {
            this.items[existingIndex].quantity += 1;
            this.items[existingIndex].lastUpdated = new Date().toISOString();
        } else {
            this.items.push({
                id: this.generateItemId(),
                code: product.code,
                name: product.name,
                price: parseFloat(product.price) || 0,
                imageUrl: product.imageUrl,
                size: product.size || null,
                quantity: 1,
                addedAt: new Date().toISOString(),
                lastUpdated: new Date().toISOString()
            });
        }
        this.saveToStorage();
        this.updateUI();
        return true;
    }
    
    removeItem(itemId) {
        this.items = this.items.filter(item => item.id !== itemId);
        this.saveToStorage();
        this.updateUI();
    }
    
    updateQuantity(itemId, newQuantity) {
        if (newQuantity <= 0) {
            this.removeItem(itemId);
            return;
        }
        
        const item = this.items.find(item => item.id === itemId);
        if (item) {
            item.quantity = newQuantity;
            item.lastUpdated = new Date().toISOString();
            this.saveToStorage();
            this.updateUI();
        }
    }
    
    clear() {
        this.items = [];
        this.saveToStorage();
        this.updateUI();
    }
    
    getTotalItems() {
        return this.items.reduce((sum, item) => sum + item.quantity, 0);
    }
    
    getTotalPrice() {
        return this.items.reduce((sum, item) => sum + (item.price * item.quantity), 0);
    }
    
    getDeliveryFee() {
        const subtotal = this.getTotalPrice();
        return subtotal >= CORE_CONFIG.ECOMMERCE.FREE_DELIVERY_THRESHOLD ? 0 : CORE_CONFIG.ECOMMERCE.DELIVERY_FEE;
    }
    
    getFinalTotal() {
        return this.getTotalPrice() + this.getDeliveryFee();
    }
    
    generateItemId() {
        return 'item_' + Date.now() + '_' + Math.random().toString(36).substr(2, 9);
    }
    
    saveToStorage() {
        try {
            const cartData = {
                items: this.items,
                lastUpdated: new Date().toISOString(),
                version: CORE_CONFIG.APP_VERSION
            };
            localStorage.setItem(CORE_CONFIG.STORAGE_KEYS.CART_DATA, JSON.stringify(cartData));
        } catch (error) {
            console.error('ÿÆÿ∑ÿ£ ŸÅŸä ÿ≠ŸÅÿ∏ ÿßŸÑÿ≥ŸÑÿ©:', error);
        }
    }
    
    loadFromStorage() {
        try {
            const cartData = localStorage.getItem(CORE_CONFIG.STORAGE_KEYS.CART_DATA);
            if (cartData) {
                const parsed = JSON.parse(cartData);
                this.items = parsed.items || [];
            }
        } catch (error) {
            console.error('ÿÆÿ∑ÿ£ ŸÅŸä ÿ™ÿ≠ŸÖŸäŸÑ ÿßŸÑÿ≥ŸÑÿ©:', error);
            this.items = [];
        }
    }
    
    updateUI() {
        this.updateCartButton();
        this.updateProductButtons();
    }
    
    updateCartButton() {
        const cartButton = document.querySelector('.cart-button');
        if (cartButton) {
            const totalItems = this.getTotalItems();
            const totalPrice = this.getTotalPrice();
            
            if (totalItems > 0) {
                cartButton.innerHTML = `
                    <span>üõí</span>
                    <span>ÿßŸÑÿ≥ŸÑÿ© (${totalItems})</span>
                    <span style="font-size: 0.9em; opacity: 0.9;">${totalPrice.toLocaleString()} ${CORE_CONFIG.ECOMMERCE.CURRENCY}</span>
                `;
                cartButton.style.background = 'linear-gradient(135deg, #ef4444 0%, #dc2626 100%)';
            } else {
                cartButton.innerHTML = `
                    <span>üõí</span>
                    <span>ÿßŸÑÿ≥ŸÑÿ© ŸÅÿßÿ±ÿ∫ÿ©</span>
                `;
                cartButton.style.background = 'linear-gradient(135deg, #25D366 0%, #128C7E 100%)';
            }
        }
    }
    
    updateProductButtons() {
        const buttons = document.querySelectorAll('.add-to-cart-btn');
        buttons.forEach(btn => {
            const productCode = btn.getAttribute('data-product-code');
            const isInCart = this.items.some(item => item.code === productCode);
            
            if (isInCart) {
                btn.classList.add('selected');
                btn.innerHTML = '‚úÖ ŸÅŸä ÿßŸÑÿ≥ŸÑÿ©';
            } else {
                btn.classList.remove('selected');
                btn.innerHTML = 'üõí ÿ£ÿ∂ŸÅ ŸÑŸÑÿ≥ŸÑÿ©';
            }
        });
    }
}

